import  apiCall  from "../../services/Api2/ApiServiceNew"
import {  API_FORGOT_PASSWORD, API_RESET_PASSWORD, BASE_URL_ } from "../../services/Api2/ApiConstants"

export const forgotpasswordApi = {



    forgotpassword(params: any, onDone: (res: any) => void, onError: (error: any, errorCode?: number) => void) {
        apiCall({ baseUrl: BASE_URL_, endPoint: API_FORGOT_PASSWORD, params, method:"post" },
            (res: any) => {
                if (onDone) {
                    onDone(res)
                }
            },
            (error, errorCode?: number) => {
                if (onError) {
                    onError(error, errorCode)
                }
            }
        )
    },
    Reestpassword(params: any, onDone: (res: any) => void, onError: (error: any, errorCode?: number) => void) {
        apiCall({ baseUrl: BASE_URL_, endPoint: API_RESET_PASSWORD, params, method:"post" },
            (res: any) => {
                if (onDone) {
                    onDone(res)
                }
            },
            (error, errorCode?: number) => {
                if (onError) {
                    onError(error, errorCode)
                }
            }
        )
    },
}
  